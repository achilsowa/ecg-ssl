def ecgt_model(model_name: str, **kwargs):
    prepare_kwargs(model_name, kwargs)
    if 'tiny' in model_name: return ecgt_tiny(**kwargs)
    elif 'small' in model_name: return ecgt_small(**kwargs)
    elif 'base' in model_name: return ecgt_base(**kwargs)
    elif 'large' in model_name: return ecgt_large(**kwargs)
    elif 'huge' in model_name: return ecgt_huge(**kwargs)
    elif 'giant' in model_name: return ecgt_giant(**kwargs)
    elif 'predictor' in model_name: return ecgt_predictor(**kwargs)
    else: raise RuntimeError("model name should be ecgt_[large|base|small|tiny|predictor], not {}".format(model_name))

def prepare_kwargs(model_name: str, kwargs):
    kwargs['patch_size'] = kwargs.get('patch_size', DEFAULT['patch_size'])
    return kwargs    


